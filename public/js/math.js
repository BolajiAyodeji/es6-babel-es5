"use strict";

module.exports = {
  sumAll: function sumAll(a, b) {
    return a + b;
  },
  subtractAll: function subtractAll(a, b) {
    return a - b;
  },
  multiplyAll: function multiplyAll(a, b) {
    return a * b;
  },
  divideAll: function divideAll(a, b) {
    return a / b;
  },
  findModulus: function findModulus(a, b) {
    return a % b;
  }
};